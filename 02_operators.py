#Operadores

print(3 + 4) #suma
print(3 - 4) #resta
print(3 * 4) #multipicación
print(3 / 4) #división
print(10 % 2) #módulo
print(10 // 3) #division aproximada (da siempre un número entero)
print(2 ** 3) #exponenciales


print("Hola" + "Python") #HolaPython
print("Hola" + str(5)) #Hola5
print("Hola" * 3) #Hola Hola Hola

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(4 == 4)
print(3 != 4)

#Operadores Lógicos

print (3 > 4 and "Hola" > "Python")
print (3 > 4 or "Hola" > "Python")
print (not(3 > 4))